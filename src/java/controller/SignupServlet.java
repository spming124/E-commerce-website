    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String email = request.getParameter("email");
        String user = request.getParameter("user");
        String pass = request.getParameter("pass");
        String cpass = request.getParameter("cpass");
        AccountDAO cdb = new AccountDAO();
        hashByMD5 hash = new hashByMD5();
        String hashPass;
        try {
            Account c = cdb.getAccountByUser(user);
            if (c == null) {
                if (pass.equals(cpass)) {
                    hashPass = hash.hash(pass);
                    Account cNew = new Account(user, hashPass, email);
                    cdb.insert(cNew);
                    request.setAttribute("mess", "Account successfully created");
                    request.getRequestDispatcher("signup.jsp").forward(request, response);
                } else {
                    request.setAttribute("mess", "Verified password uncorrect!");
                    request.getRequestDispatcher("signup.jsp").forward(request, response);
                }
            } else {
                request.setAttribute("mess", "Username already exists!");
                request.getRequestDispatcher("signup.jsp").forward(request, response);
            }

        } catch (NumberFormatException e) {
            System.out.println(e);
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

